#include "libft.h"

int	ft_isnum(char *s)
{
	if (!s)
		return (0);
	if (*s == '-')
		s++;
	if (!*s)
		return (0);
	while (*s)
	{
		if (!ft_isdigit(*s))
			return (0);
		s++;
	}
	return (1);
}
