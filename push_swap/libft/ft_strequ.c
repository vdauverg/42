int	ft_strequ(const char *s1, const char *s2)
{
	int	i;

	i = 0;
	if (s1 && s2)
	{
		while (s1[i])
		{
			if (s1[i] != s2[i])
				return (0);
			i++;
		}
		if (!s2[i])
			return (1);
	}
	return (0);
}
